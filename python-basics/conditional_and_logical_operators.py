# Conditional statements, or conditionals, let you control the flow of your program based on whether certain conditions are true or false.

# == Equal
# ! = Not equal
# > Greater than
# < Less than
# > = Greater than or equal
# < = Less than or equal

print(3 > 4)  # False
print(3 < 4)  # True
print(3 == 4)  # False
print(4 == 4)  # True
print(3 != 4)  # True
print(3 >= 4)  # False
print(3 <= 4)  # True
